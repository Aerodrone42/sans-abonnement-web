
import { useState, useRef, useEffect } from 'react';
import { EnhancedChatGPTService } from '@/services/enhancedChatGptService';
import { SpeechSynthesisService } from '@/services/speechSynthesisService';

interface UseVoiceRecognitionProps {
  onTranscript: (text: string, field: string) => void;
  conversationMode: boolean;
  chatGPT: EnhancedChatGPTService | null;
}

interface ExtendedSpeechRecognition extends SpeechRecognition {
  continuous: boolean;
  interimResults: boolean;
  lang: string;
  maxAlternatives: number;
  onstart: ((this: SpeechRecognition, ev: Event) => any) | null;
  onend: ((this: SpeechRecognition, ev: Event) => any) | null;
  onerror: ((this: SpeechRecognition, ev: SpeechRecognitionErrorEvent) => any) | null;
  onresult: ((this: SpeechRecognition, ev: SpeechRecognitionEvent) => any) | null;
}

export const useVoiceRecognition = ({ onTranscript, conversationMode, chatGPT }: UseVoiceRecognitionProps) => {
  const [isListening, setIsListening] = useState(false);
  const [transcript, setTranscript] = useState("");
  const [isProcessing, setIsProcessing] = useState(false);
  const [lastResponse, setLastResponse] = useState("");
  const [isSpeaking, setIsSpeaking] = useState(false);
  const [isConversationActive, setIsConversationActive] = useState(false);
  
  const recognitionRef = useRef<ExtendedSpeechRecognition | null>(null);
  const mediaStreamRef = useRef<MediaStream | null>(null);
  const speechSynthesis = useRef(new SpeechSynthesisService()).current;
  const restartTimeoutRef = useRef<NodeJS.Timeout | null>(null);
  const silenceTimeoutRef = useRef<NodeJS.Timeout | null>(null);
  
  const shouldContinueRef = useRef(false);
  const processingRef = useRef(false);
  const speakingRef = useRef(false);
  const lastTranscriptRef = useRef("");
  const interimResultRef = useRef("");
  const isStoppedRef = useRef(true);
  const microphoneMutedRef = useRef(false);
  const recognitionActiveRef = useRef(false);
  const conversationActiveRef = useRef(false);

  const cleanup = () => {
    console.log('üßπ Nettoyage complet microphone');
    shouldContinueRef.current = false;
    speakingRef.current = false;
    isStoppedRef.current = true;
    microphoneMutedRef.current = false;
    recognitionActiveRef.current = false;
    conversationActiveRef.current = false;
    setIsListening(false);
    setIsConversationActive(false);
    
    // CORRECTION: Nettoyer tous les timeouts pour √©viter les red√©marrages
    if (restartTimeoutRef.current) {
      clearTimeout(restartTimeoutRef.current);
      restartTimeoutRef.current = null;
    }
    
    if (silenceTimeoutRef.current) {
      clearTimeout(silenceTimeoutRef.current);
      silenceTimeoutRef.current = null;
    }
    
    if (mediaStreamRef.current) {
      mediaStreamRef.current.getTracks().forEach(track => track.stop());
      mediaStreamRef.current = null;
    }

    if (recognitionRef.current) {
      try {
        recognitionRef.current.stop();
      } catch (error) {
        console.log('Recognition d√©j√† arr√™t√©e');
      }
    }
  };

  const stopSpeaking = () => {
    console.log('üõë ARR√äT COMPLET DEMAND√â');
    cleanup();
    speechSynthesis.stop();
    setIsSpeaking(false);
    setIsProcessing(false);
    processingRef.current = false;
  };

  const muteMicrophoneForSpeech = () => {
    console.log('üé§‚ùå MICRO FORC√â ARR√äT - IA va parler');
    microphoneMutedRef.current = true;
    recognitionActiveRef.current = false;
    setIsListening(false);
    
    if (recognitionRef.current) {
      try {
        recognitionRef.current.stop();
        console.log('üîá Reconnaissance vocale FORC√âE ARR√äT pendant synth√®se');
      } catch (error) {
        console.log('Erreur arr√™t recognition pour synth√®se');
      }
    }
  };

  const unmuteMicrophoneAfterSpeech = () => {
    console.log('üé§‚úÖ MICRO R√âACTIV√â - IA a fini de parler');
    microphoneMutedRef.current = false;
    
    setTimeout(() => {
      // CORRECTION: V√©rifications plus strictes pour √©viter les red√©marrages intempestifs
      if (conversationActiveRef.current && 
          !isStoppedRef.current && 
          !speakingRef.current && 
          !processingRef.current && 
          !recognitionActiveRef.current &&
          !microphoneMutedRef.current) {
        console.log('üîÑ Red√©marrage micro apr√®s synth√®se');
        startRecognitionSafely();
      } else {
        console.log('üö´ Red√©marrage micro annul√© - conditions non remplies');
      }
    }, 1000);
  };

  const startRecognitionSafely = () => {
    if (!recognitionRef.current) {
      console.log('‚ùå Recognition non disponible');
      return false;
    }

    if (recognitionActiveRef.current) {
      console.log('‚ö†Ô∏è Recognition d√©j√† active, ignorer le d√©marrage');
      return false;
    }

    // CORRECTION: V√©rifier que la conversation est active
    if (!conversationActiveRef.current) {
      console.log('‚ùå Conversation non active, arr√™t du d√©marrage');
      return false;
    }

    try {
      shouldContinueRef.current = true;
      recognitionActiveRef.current = true;
      recognitionRef.current.start();
      console.log('‚úÖ Recognition d√©marr√©e avec succ√®s');
      return true;
    } catch (error) {
      console.error('‚ùå Erreur d√©marrage recognition:', error);
      recognitionActiveRef.current = false;
      setIsListening(false);
      return false;
    }
  };

  const processAIResponse = async (finalTranscript: string) => {
    console.log('ü§ñ D√âBUT TRAITEMENT IA:', finalTranscript);
    
    // CORRECTION: V√©rifications plus strictes
    if (isStoppedRef.current || !conversationActiveRef.current) {
      console.log('‚ùå Traitement annul√© - conversation arr√™t√©e');
      return;
    }
    
    if (!finalTranscript || finalTranscript.trim().length < 2) {
      console.log('‚ùå Transcript trop court, ignor√©:', finalTranscript);
      return;
    }

    console.log('üî• ACTIVATION PROCESSING');
    setIsProcessing(true);
    processingRef.current = true;
    
    muteMicrophoneForSpeech();

    if (!chatGPT) {
      console.log('‚ùå ChatGPT non initialis√©, mode dict√©e seulement');
      setTimeout(() => {
        setIsProcessing(false);
        processingRef.current = false;
        unmuteMicrophoneAfterSpeech();
        onTranscript(finalTranscript, "message");
      }, 500);
      return;
    }
    
    if (!conversationMode) {
      console.log('Mode dict√©e activ√©');
      setTimeout(() => {
        setIsProcessing(false);
        processingRef.current = false;
        unmuteMicrophoneAfterSpeech();
        onTranscript(finalTranscript, "message");
      }, 1500);
      return;
    }

    try {
      console.log('üì§ Envoi √† ChatGPT...');
      const response = await chatGPT.sendMessage(finalTranscript);
      
      console.log('‚úÖ R√©ponse ChatGPT re√ßue:', response);
      
      setIsSpeaking(true);
      speakingRef.current = true;
      
      console.log('üîä D√©but synth√®se vocale');
      speechSynthesis.speak(response, () => {
        console.log('‚úÖ Synth√®se termin√©e');
        setIsSpeaking(false);
        speakingRef.current = false;
        setIsProcessing(false);
        processingRef.current = false;
        unmuteMicrophoneAfterSpeech();
      });
      
    } catch (error) {
      console.error('‚ùå Erreur ChatGPT:', error);
      setIsProcessing(false);
      processingRef.current = false;
      setTimeout(() => {
        unmuteMicrophoneAfterSpeech();
      }, 2000);
    }
  };

  const startListening = async () => {
    console.log('üéØ D√âMARRAGE conversation');
    
    if (!recognitionRef.current) {
      console.log('‚ùå Recognition indisponible');
      return;
    }

    if (isSpeaking) {
      speechSynthesis.stop();
      setIsSpeaking(false);
      speakingRef.current = false;
    }

    try {
      console.log('üé§ Demande permission micro...');
      if (!mediaStreamRef.current) {
        const stream = await navigator.mediaDevices.getUserMedia({ 
          audio: {
            echoCancellation: true,
            noiseSuppression: true,
            autoGainControl: true
          }
        });
        mediaStreamRef.current = stream;
        console.log('‚úÖ Permission micro obtenue');
      }

      isStoppedRef.current = false;
      microphoneMutedRef.current = false;
      conversationActiveRef.current = true;
      setIsConversationActive(true);

      console.log('üöÄ D√©marrage reconnaissance vocale');
      const started = startRecognitionSafely();
      
      if (started) {
        console.log('‚úÖ Conversation ACTIVE');
      } else {
        console.log('‚ùå √âchec d√©marrage conversation');
      }
    } catch (error) {
      console.error('‚ùå Erreur d√©marrage:', error);
      cleanup();
    }
  };

  const stopListening = () => {
    console.log('üõë ARR√äT conversation demand√©');
    conversationActiveRef.current = false;
    setIsConversationActive(false);
    stopSpeaking();
  };

  useEffect(() => {
    console.log('üîÑ Initialisation reconnaissance vocale');
    
    if ('webkitSpeechRecognition' in window || 'SpeechRecognition' in window) {
      const SpeechRecognitionClass = (window as any).SpeechRecognition || (window as any).webkitSpeechRecognition;
      const recognition = new SpeechRecognitionClass() as ExtendedSpeechRecognition;
      recognitionRef.current = recognition;
      
      recognition.continuous = true;
      recognition.interimResults = true;
      recognition.lang = 'fr-FR';
      recognition.maxAlternatives = 1;

      recognition.onstart = () => {
        console.log('üé§ Recognition d√©marr√©e - √âCOUTE ACTIVE');
        recognitionActiveRef.current = true;
        setIsListening(true);
      };

      recognition.onresult = (event) => {
        console.log('üéØ R√âSULTAT VOCAL RE√áU');
        let finalTranscript = '';
        let interimTranscript = '';
        
        for (let i = event.resultIndex; i < event.results.length; i++) {
          const transcript = event.results[i][0].transcript;
          if (event.results[i].isFinal) {
            finalTranscript += transcript;
          } else {
            interimTranscript += transcript;
          }
        }
        
        if (interimTranscript) {
          interimResultRef.current = interimTranscript;
          const displayText = lastTranscriptRef.current + interimTranscript;
          setTranscript(displayText);
          console.log('üìù AFFICHAGE INTERIM:', displayText);
        }
        
        if (finalTranscript.trim()) {
          console.log('üéØ TRANSCRIPT FINAL:', finalTranscript);
          lastTranscriptRef.current += finalTranscript;
          setTranscript(lastTranscriptRef.current);
          
          if (silenceTimeoutRef.current) {
            console.log('‚è∞ Annulation timeout pr√©c√©dent');
            clearTimeout(silenceTimeoutRef.current);
            silenceTimeoutRef.current = null;
          }
          
          console.log('‚è∞ NOUVEAU TIMEOUT de 3s pour traitement IA');
          silenceTimeoutRef.current = setTimeout(() => {
            console.log('‚è∞ TIMEOUT D√âCLENCH√â - V√©rification conditions');
            
            // CORRECTION: Conditions plus strictes et claires
            const canProcess = conversationActiveRef.current && 
                              !isStoppedRef.current && 
                              !microphoneMutedRef.current && 
                              !processingRef.current &&
                              lastTranscriptRef.current.trim();
            
            console.log('üîç CONDITIONS:', {
              conversationActive: conversationActiveRef.current,
              notStopped: !isStoppedRef.current,
              micNotMuted: !microphoneMutedRef.current,
              notProcessing: !processingRef.current,
              hasTranscript: !!lastTranscriptRef.current.trim()
            });
            
            if (canProcess) {
              console.log('üöÄ LANCEMENT TRAITEMENT IA');
              processAIResponse(lastTranscriptRef.current.trim());
              lastTranscriptRef.current = "";
              interimResultRef.current = "";
              setTranscript("");
            } else {
              console.log('‚ùå CONDITIONS NON REMPLIES pour traitement IA');
            }
          }, 3000);
        }
      };

      recognition.onerror = (event) => {
        console.error('‚ùå Erreur recognition:', event.error);
        recognitionActiveRef.current = false;
        setIsListening(false);
        
        if (event.error === 'not-allowed') {
          console.error('‚ùå Permission microphone refus√©e');
          conversationActiveRef.current = false;
          setIsConversationActive(false);
          return;
        }
        
        // CORRECTION: Ne red√©marrer que si vraiment n√©cessaire
        if (conversationActiveRef.current && 
            !processingRef.current && 
            !speakingRef.current && 
            !isStoppedRef.current && 
            !microphoneMutedRef.current) {
          console.log('üîÑ Red√©marrage apr√®s erreur dans 1s');
          restartTimeoutRef.current = setTimeout(() => {
            if (conversationActiveRef.current && 
                !speakingRef.current && 
                !processingRef.current && 
                !isStoppedRef.current && 
                !microphoneMutedRef.current) {
              startRecognitionSafely();
            }
          }, 1000);
        }
      };

      recognition.onend = () => {
        console.log('üèÅ Recognition termin√©e');
        recognitionActiveRef.current = false;
        setIsListening(false);
        
        // CORRECTION: Ne red√©marrer que si toutes les conditions sont remplies
        if (conversationActiveRef.current && 
            !processingRef.current && 
            !speakingRef.current && 
            !isStoppedRef.current && 
            !microphoneMutedRef.current) {
          console.log('üîÑ Auto-restart recognition dans 500ms');
          restartTimeoutRef.current = setTimeout(() => {
            if (conversationActiveRef.current && 
                !speakingRef.current && 
                !processingRef.current && 
                !isStoppedRef.current && 
                !microphoneMutedRef.current && 
                !recognitionActiveRef.current) {
              startRecognitionSafely();
            }
          }, 500);
        } else {
          console.log('üö´ Auto-restart ignor√© - conditions non remplies');
        }
      };
      
      console.log('üéôÔ∏è Speech Recognition configur√©e et PR√äTE');
    }

    return cleanup;
  }, []);

  return {
    isListening,
    transcript,
    isProcessing,
    lastResponse,
    isSpeaking,
    isConversationActive,
    startListening,
    stopListening,
    stopSpeaking,
    cleanupMicrophone: cleanup
  };
};
